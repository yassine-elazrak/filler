#include "libft.h"

char	*ft_strnew(size_t n)
{
	char	*t;
	size_t	i;

	i = 0;
	if (!(t = (char *)malloc(sizeof(char) * (1 + n))))
		return (NULL);
	while (i <= n)
	{
		t[i] = '\0';
		i++;
	}
	return (t);
}
